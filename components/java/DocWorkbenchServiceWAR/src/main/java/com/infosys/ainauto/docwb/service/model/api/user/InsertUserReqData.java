package com.infosys.ainauto.docwb.service.model.api.user;

public class InsertUserReqData {
	private String userEmail;
	private String userName;
	private String userPassword;
	private String userFullName;
	private int userTypeCde;
	private String tenantId;

	
	public String getTenantId() {
		return tenantId;
	}
	public void setTenantId(String tenantId) {
		this.tenantId = tenantId;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserFullName() {
		return userFullName;
	}
	public void setUserFullName(String userFullName) {
		this.userFullName = userFullName;
	}
	public int getUserTypeCde() {
		return userTypeCde;
	}
	public void setUserTypeCde(int userTypeCde) {
		this.userTypeCde = userTypeCde;
	}
}
