package com.infosys.ainauto.docwb.engine.core.stereotype;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface ActionScriptResultUpdater {
	String title() default "Action Script Result Updater";

	/**
	 * @return Location of properties file which should be on classpath
	 */
	String propertiesFile() default "";
}
